package it.unirc.ing.alg_sdati.compito;

public class Corridoio {

	private int id;
	private Integer capacita;
	
	
	public Corridoio(int id, Integer capacita) {
		super();
		this.id = id;
		this.capacita = capacita;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public Integer getCapacita() {
		return capacita;
	}
	public void setCapacita(Integer capacita) {
		this.capacita = capacita;
	}
	
	
}
